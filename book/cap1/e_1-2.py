"""Inicialize o interpretador do Python e use-o como uma calculadora.

    Q1 - Quantos segundos há em 42 minutos e 42 segundos?

    Q2 - Quantas milhas há em 10 quilômetros? Dica: uma milha equivale a 1,61 quilômetro.

    Q3 - Se você correr 10 quilômetros em 42 minutos e 42 segundos, qual é o seu passo médio (tempo por milha em minutos e segundos)? Qual é a sua velocidade média em milhas por hora?
"""
seconds = 42 * 60 + 42
print(f'Q1 = {seconds}')


miles = 10 / 1.61
